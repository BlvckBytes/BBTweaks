  private static void registerMaterial(int id, int subId, Material material) {
    if (subId == 0) {
      if (materialByNotation.put(String.valueOf(id), material) != null)
        throw new IllegalStateException("Collision for " + id);
    }

    if (materialByNotation.put(id + ":" + subId, material) != null)
      throw new IllegalStateException("Collision for " + id + ":" + subId);
  }

  public static @Nullable Material tryGetMaterialByNotation(String notation) {
    return materialByNotation.get(notation);
  }
